


public static void main (String[] args)   {
    byte a=4;
    System.out.println("a = " + a);

    int b=4;
    System.out.println("b = " + b);

    float f=4.4F;
    System.out.println("f = " + f);

    double l=4.6;
    System.out.println("l = " + l);

    String q="Hello";
    System.out.println("q = " + q);

    boolean w=false;
    System.out.println("w = " + w);




}